    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed

        String email = tf_email.getText();
    
        // Pastikan email tidak kosong
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email is required", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    
        // Konfirmasi sebelum menghapus data
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete your profile?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            boolean isDeleted = Database.deleteUserProfile(email);
        
        if (isDeleted) {
            JOptionPane.showMessageDialog(this, "Profile deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            
            // Menutup JFrame Profile
            this.dispose(); // Menutup jendela Profile
            
            // Menampilkan JFrame Login setelah data dihapus
            java.awt.EventQueue.invokeLater(() -> {
                new LoginJframe().setVisible(true); // Menampilkan Login JFrame
            });
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete profile", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


    }//GEN-LAST:event_btn_deleteActionPerformed

