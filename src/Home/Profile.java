    private void loadProfileData(String email) {
    // Mengambil data user dari database berdasarkan email
    User user = Database.getUserData(email);

    if (user != null) {
        // Menampilkan data pengguna di JTextField atau JLabel sesuai dengan yang diperlukan
        tf_email.setText(user.getEmail());
        tf_name.setText(user.getName());
        tf_npm.setText(user.getNpm());
        tf_gender.setText(user.getGender());
        tf_faculty.setText(user.getFaculty());
        tf_program.setText(user.getProgram());
        tf_description.setText(user.getDescription());
        tf_link.setText(user.getLink());
        
        Database.tampilGambar(lb_photop, email);
             
    } else {
        JOptionPane.showMessageDialog(this, "User data not found.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}

